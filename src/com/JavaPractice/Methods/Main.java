package com.JavaPractice.Methods;

public class Main {
    public static void main(String[] args) {
        sayHello();
    }

    /*
          Syntax of a method:
            <access modifier> <return type> <method name> (<parameter list>) {
                // method body
                return <return value>;
            }
     */

    static void sayHello() {
        System.out.println("Hello");
    }
}
