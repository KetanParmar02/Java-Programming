package com.JavaPractice.PrimitivesDataType;

public class Primitives {
    public static void main(String[] args) {
//        Primitives Data Type Example
        int num = 45;
        float marks = 567.437f;
        double number = 48437.743982;
        long numb = 4637863543L;
        char ch = 'a';
        boolean value = true;

        System.out.println(num+" "+marks+" "+number+" "+numb+" "+ch+" "+value);
    }
}
